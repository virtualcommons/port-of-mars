<template>
  <div class="round">
    <p class="v-step-2">
      Round <span>{{ roundNumber }}</span>
    </p>
  </div>
</template>

<script lang="ts">
import { Vue, Component, Prop } from 'vue-property-decorator';

@Component
export default class Round extends Vue {
  get roundNumber() {
    const { round, upkeep } = this.$store.state;
    if (round > 0 && upkeep > 0) {
      return round;
    }

    if (upkeep <= 0) {
      return 'Over';
    }

    return 'Pregame';
  }
}
</script>

